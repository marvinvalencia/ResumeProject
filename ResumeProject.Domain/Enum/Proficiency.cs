namespace ResumeProject.Domain.Enum
{
    /// <summary>
    /// The proficiency levels for skills in a resume.
    /// </summary>
    public enum Proficiency
    {
        /// <summary>
        /// The Beginner level of proficiency, typically represented by 1 star.
        /// </summary>
        Beginner = 1,

        /// <summary>
        /// The Intermediate level of proficiency, typically represented by 2 stars.
        /// </summary>
        Intermediate = 2,

        /// <summary>
        /// The Advanced level of proficiency, typically represented by 3 stars.
        /// </summary>
        Advanced = 3,

        /// <summary>
        /// The Expert level of proficiency, typically represented by 4 stars.
        /// </summary>
        Expert = 4,

        /// <summary>
        /// The Master level of proficiency, typically represented by 5 stars.
        /// </summary>
        Master = 5,
    }
}
